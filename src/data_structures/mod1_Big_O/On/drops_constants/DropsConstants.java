package data_structures.mod1_Big_O.On.drops_constants;

public class DropsConstants {
    public static void main(String[] args) {
        imprimir(10);
    }

    public static void imprimir(int n) {
        for (int i = 0; i < n; i++) {
            System.out.println(i);
        }

        for (int j = 0; j < n; j++) {
            System.out.println(j);
        }
    }
}
